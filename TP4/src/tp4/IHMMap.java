    private void jPanel1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPanel1KeyPressed
        
        if(evt.getKeyChar()== 'z'){
            indiceLink = 10; 
            compteurLink += 1; 
            yLink -= 10;} //vers le haut
            
         if(evt.getKeyChar()== 'q'){
            indiceLink = 0; 
            compteurLink += 1; 
            xLink -= 10;} //vers la gauche
        
         if(evt.getKeyChar()== 's'){
             indiceLink = 15; 
             compteurLink += 1; 
            yLink += 10;} //vers le bas
            
         if(evt.getKeyChar()== 'd'){
             indiceLink = 5; 
             compteurLink += 1; 
            xLink += 10;} // vers la droite
         
        
         if(compteurLink == 5){
             compteurLink = 0;
         }
         
        traitementPotion();
        traitementBornes();
      
        jPanel1.repaint();
    }//GEN-LAST:event_jPanel1KeyPressed
    
    private void traitementBornes()
    {
        
        if(xLink < -90){ //90 rpz l'orientation du perso 
            xLink = 1800;
        }
        
        if(xLink > 1800){
            xLink = -90;
        }
        
        if(yLink < -90){
            yLink = 1038;    
        }
        
        if(yLink > 1038){
            yLink = -90;  
        }
    }
